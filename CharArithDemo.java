class CharArithDemo {
    public static void main(String ags[]) {
        char ch;
        
        ch = 'X';
        System.out.println("ch содержит " + ch);
        
        ch++; // инкременировать переменную ch (увеличение на 1)
        System.out.println("теперь ch содержит " + ch);

        ch = 90; // присвоить переменной ch значение Z под цифрой 90 в Unicode
        System.out.println("теперь ch содержит " + ch);
    }
}